package operators;

public class BitwiseOperators {

	public static void main(String[] args) {
		int a = 5; // 1 0 1 
		int b = 3; // 0 1 1
		int c = -5;
		// AND Operator ---> &
		System.out.println(a&b);
		
		// OR Operator ---> |
		System.out.println(a|b);
		
		// XOR Operator ---> ^
		System.out.println(a^b);
		
		// NOT Operator ---> ~
		System.out.println(~a);
		
		System.out.println(~c);
		
		
		
		
		

	}

}
